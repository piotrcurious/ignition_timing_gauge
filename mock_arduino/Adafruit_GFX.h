class Adafruit_GFX {
public:
    int16_t width, height;
    Adafruit_GFX(int16_t w, int16_t h) : width(w), height(h), _textSize(1), _textColor(WHITE), _cursorX(0), _cursorY(0) {}
    virtual void drawPixel(int16_t x, int16_t y, uint16_t color) = 0;

    void drawLine(int16_t x0, int16_t y0, int16_t x1, int16_t y1, uint16_t color) {
        int16_t dx = abs(x1 - x0), sx = x0 < x1 ? 1 : -1;
        int16_t dy = -abs(y1 - y0), sy = y0 < y1 ? 1 : -1;
        int16_t err = dx + dy, e2;
        for (;;) {
            drawPixel(x0, y0, color);
            if (x0 == x1 && y0 == y1) break;
            e2 = 2 * err;
            if (e2 >= dy) { err += dy; x0 += sx; }
            if (e2 <= dx) { err += dx; y0 += sy; }
        }
    }

    void fillRect(int16_t x, int16_t y, int16_t w, int16_t h, uint16_t color) {
        for (int16_t i = x; i < x + w; i++) {
            for (int16_t j = y; j < y + h; j++) {
                drawPixel(i, j, color);
            }
        }
    }

    void fillCircle(int16_t x0, int16_t y0, int16_t r, uint16_t color) {
        for (int16_t x = x0 - r; x <= x0 + r; x++) {
            for (int16_t y = y0 - r; y <= y0 + r; y++) {
                if ((x - x0) * (x - x0) + (y - y0) * (y - y0) <= r * r) {
                    drawPixel(x, y, color);
                }
            }
        }
    }

    void setCursor(int16_t x, int16_t y) { _cursorX = x; _cursorY = y; }
    void setTextSize(uint8_t s) { _textSize = s; }
    void setTextColor(uint16_t c) { _textColor = c; }

    void drawChar(int16_t x, int16_t y, unsigned char c, uint16_t color, uint16_t bg, uint8_t size) {
        if (c < 32 || c > 126) return;
        uint8_t font_idx = c - 32;

        for (int8_t i = 0; i < 5; i++) {
            uint8_t line = font5x7[font_idx * 5 + i];
            for (int8_t j = 0; j < 8; j++) {
                if (line & 0x01) {
                    if (size == 1) drawPixel(x + i, y + j, color);
                    else fillRect(x + i * size, y + j * size, size, size, color);
                } else if (bg != color) {
                    if (size == 1) drawPixel(x + i, y + j, bg);
                    else fillRect(x + i * size, y + j * size, size, size, bg);
                }
                line >>= 1;
            }
        }
    }

    size_t write(uint8_t c) {
        if (c == '\n') {
            _cursorY += _textSize * 8;
            _cursorX = 0;
        } else if (c == '\r') {
        } else {
            drawChar(_cursorX, _cursorY, c, _textColor, BLACK, _textSize);
            _cursorX += _textSize * 6;
        }
        return 1;
    }

    void print(const char* s) { while (*s) write(*s++); }
    void print(std::string s) { for (char c : s) write(c); }
    void print(int n) { print(std::to_string(n)); }
    void print(long n) { print(std::to_string(n)); }
    void print(unsigned long n) { print(std::to_string(n)); }
    void print(double n) {
        char buf[32];
        snprintf(buf, sizeof(buf), "%.1f", n);
        print(buf);
    }
    void print(float n) { print((double)n); }

    void println(const char* s) { print(s); write('\n'); }
    void println(std::string s) { print(s); write('\n'); }
    void println(int n) { print(n); write('\n'); }
    void println(long n) { print(n); write('\n'); }
    void println(unsigned long n) { print(n); write('\n'); }
    void println(float n) { print(n); write('\n'); }
    void println(double n) { print(n); write('\n'); }
    void println() { write('\n'); }

    template<typename... Args>
    void drawBitmap(Args... args) {}

protected:
    uint8_t _textSize;
    uint16_t _textColor;
    int16_t _cursorX, _cursorY;
};

